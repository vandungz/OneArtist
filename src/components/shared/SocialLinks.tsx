export interface SocialLink {
    label: string
    href: string
    icon: 'instagram' | 'spotify' | 'youtube' | 'soundcloud' | 'mail'
    isContact?: boolean
}

interface SocialLinksProps {
    links: SocialLink[]
    variant?: 'default' | 'grid'
}

const iconMap = {
    instagram: InstagramIcon,
    spotify: SpotifyIcon,
    youtube: YouTubeIcon,
    soundcloud: SoundCloudIcon,
    mail: MailIcon,
}

export function SocialLinks({ links, variant = 'default' }: SocialLinksProps) {
    return (
        <div className={`home-card-socials ${variant === 'grid' ? 'grid-2' : ''}`}>
            <ul>
                {links.map((link, index) => {
                    const IconComponent = iconMap[link.icon]

                    return (
                        <li key={index}>
                            <Link href={link.href}>
                                <p>{link.label}</p>
                                <div className="socials-icon">
                                    <IconComponent />
                                </div>
                            </Link>
                        </li>
                    )
                })}
            </ul>
        </div>
    )
}

